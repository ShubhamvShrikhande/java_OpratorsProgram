package ArtithmaticOpreators;

public class UnaryOpratore {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("the value of a :" + a++);
        System.out.println("the value of a :" + a--);
        System.out.println("the value of a :" + ++a);
        System.out.println("the value of a :" + --a);

    }
}
